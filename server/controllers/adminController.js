// Add a new car to the platform
export const addCar = async (req, res) => {
  try {
    const {
      brand,
      model,
      year,
      registrationNumber,
      type,
      color,
      rentalPricePerDay,
      fuelType,
      transmission,
      seats,
      status,
      mileage,
      description,
      images,
      currentLocation,
    } = req.body;

    // You might also extract the user ID from req.user if you have user authentication.
    const ownerId = req.user._id;

    const isCarExist = await Car.findOne({ registrationNumber });
    if (isCarExist) {
      return res.status(400).json({ message: 'Car with this registration number already exists' });
    }

    const newCar = new Car({
      brand,
      model,
      year,
      registrationNumber,
      type,
      color,
      rentalPricePerDay,
      fuelType,
      transmission,
      seats,
      status,
      mileage,
      description,
      images,
      currentLocation,
      ownerId, // owner is the mediator or admin adding the car
    });


    await newCar.save();

  
    return res.status(201).json({
      message: 'Car added successfully',
      car: newCar,
    });
  } catch (error) {
    console.error(error);
    return res.status(500).json({ message: 'Failed to add car' });
  }
};


