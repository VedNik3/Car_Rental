// Manage Users:

//Get all users
export const getAllUsers = async (req, res) => {
  try {
    const users = await User.find().select("-password");
    res.json(users);
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};

// Update a user role
export const changeUserRole = async (req, res) => {
  try {
    const { email, newRole } = req.body; //admin will give new role in form/button
    const user = await User.findOne({ email });

    if (!user) {
      res.status(401).json({ message: "User does not exist" });
    }

    user.role = newRole;
    await user.save();
    res.status(200).json({ message: "User role has changed successfully" });
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};

// deleteUser(userId): Remove a user from the platform.
export const deleteUser = async (req, res) => {
  try {
    const user_id = req.params.id;

    const user = await User.findById(user_id);
    if (!user) {
      return res.status(404).json({ message: "User not found" });
    }

    await User.findByIdAndDelete(user_id);
    res.status(200).json({ message: "User has deleted successfully" });
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};

